function buildRecentActionCooldownInline(recentActionHints: readonly string[]): string {
  const hints = normalizeRecentActionHints(recentActionHints)
  if (hints.length === 0) return ''
  return ` 最近 3 次同品类生成已经出现过这些动作/表情族：${hints.join('、')}。本轮请显著降低它们的出现频率，优先刷新为其它自然动作；类似动作可以在之后第 3-4 次生成再回流。`
}

