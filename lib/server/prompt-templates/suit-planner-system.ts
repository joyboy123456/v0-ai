export function getSuitShotBlueprintForCount(
  resultCount: PhotoFissionResultCount,
): ReadonlyArray<SuitShotBlueprint> {
  if (resultCount === 2) {
    return SUIT_TWO_SHOT_BLUEPRINT
  }
  if (resultCount === 4) {
    return SUIT_FOUR_SHOT_BLUEPRINT
  }

  const outdoorCount = resultCount >= 9 ? 2 : 0
  const referenceCount = resultCount - outdoorCount
  const backShot = SUIT_REFERENCE_SHOT_POOL.find((shot) => shot.label === '背面与侧后轮廓')
  const requiresBackShot = resultCount >= 9 && backShot !== undefined
  const referencePool = requiresBackShot
    ? SUIT_REFERENCE_SHOT_POOL.filter((shot) => shot !== backShot)
    : SUIT_REFERENCE_SHOT_POOL
  const referenceSlots = Math.max(0, referenceCount - (requiresBackShot ? 1 : 0))
  const referenceShots = [
    ...(requiresBackShot && backShot ? [backShot] : []),
    ...takeRandom(referencePool, Math.min(referenceSlots, referencePool.length)),
  ]
  const outdoorShots =
    outdoorCount > 0
      ? takeRandom(SUIT_OUTDOOR_SHOT_POOL, Math.min(outdoorCount, SUIT_OUTDOOR_SHOT_POOL.length))
      : []

  return shuffle([...referenceShots, ...outdoorShots])
}

function takeRandom<T>(items: ReadonlyArray<T>, count: number): T[] {
  return shuffle([...items]).slice(0, count)
}

function shuffle<T>(items: T[]): T[] {
  for (let index = items.length - 1; index > 0; index -= 1) {
    const swapIndex = Math.floor(Math.random() * (index + 1))
    const current = items[index]
    items[index] = items[swapIndex]
    items[swapIndex] = current
  }
  return items
}

