function normalizeRecentActionHints(recentActionHints: readonly string[]): string[] {
  return [...new Set(recentActionHints.map((hint) => hint.trim()).filter(Boolean))].slice(0, 12)
}

function buildSuitShotJsonExample(resultCount: number): string {
  const roles = buildSuitShotRoles(resultCount)
  const entries = Array.from({ length: resultCount }, (_, index) => {
    const shotId = `shot_${index + 1}`
    const role = roles[index] ?? `套装裂变图 ${index + 1}`
    return `    { "shotId": "${shotId}", "role": "${role}", "imagePrompt": "..." }`
  }).join(',\n')

  return `{\n  "shots": [\n${entries}\n  ]\n}`
}

