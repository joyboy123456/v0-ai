/**
 * 按 resultCount 生成 Planner 系统提示词。
 *
 * 所有数量都用明确的数量规则块替换，避免小数量模式残留外景规则，
 * 也让 9/10 张模式稳定包含 1 张金属椅坐姿 + 1 张背面展示变化位 + 1 张外景站姿图 + 1 张草地坐姿图。
 */
export function buildChildrensDressPlannerSystemPrompt(
  resultCount: number,
): string {
  const n = resultCount
  const { reference: refCount, outdoor: outCount } = getSceneDistribution(n)

  let prompt = CHILDRENS_DRESS_PLANNER_SYSTEM_PROMPT

  const sceneRule = buildSceneRuleText(n, refCount, outCount)
  const shelfSceneRule = buildShelfSceneRuleText(n, refCount, outCount)
  const shotStructureRule = buildShotStructureRuleText(n, refCount, outCount)
  const croppingRule = buildCroppingRuleText(n)
  const backgroundLockRule = buildBackgroundLockRuleText(n, refCount, outCount)

  // 开头：输出 N 段
  prompt = prompt.replace(
    '你为这套连衣裙输出\n9 段商品展示优先的童装上架大片提示词',
    `你为这套连衣裙输出\n${n} 段商品展示优先的童装上架大片提示词`,
  )
  prompt = prompt.replace('9 段之间也要变化', `${n} 段之间也要变化`)

  // 背景常量：场景分布
  prompt = prompt.replace(
    /本轮 9 张中必须有 7 张沿用参考图拍摄环境基调[\s\S]*?不能替代核心棚拍商品图。/,
    sceneRule,
  )

  // 标题：N 张图随机裂变规则
  prompt = prompt.replace('9 张图随机裂变规则', `${n} 张图随机裂变规则`)
  prompt = prompt.replace('shot_1 到 shot_9', `shot_1 到 shot_${n}`)

  // 分镜结构 + 场景硬约束 + 姿势要求
  prompt = prompt.replace(
    /\*\*唯一的硬约束（场景比例）\*\*：[\s\S]*?\n# 裁切规则/,
    `${shotStructureRule}\n\n# 裁切规则`,
  )

  // 裁切规则必须与数量模式一致，所有模式都禁半身 / 纯局部。
  prompt = prompt.replace(
    /# 裁切规则[\s\S]*?\n# 电商货架感规则/,
    `${croppingRule}\n\n# 电商货架感规则`,
  )

  // 电商货架感里的场景分布必须与硬约束一致。
  prompt = prompt.replace(
    /2\. \*\*7 张参考背景延续但弱化\*\*：[\s\S]*?\n4\. \*\*上架图构图\*\*：/,
    `${shelfSceneRule}\n4. **上架图构图**：`,
  )

  // 提示词风格铁律里的背景锁定也要同步数量规则。
  prompt = prompt.replace(
    /5\. \*\*背景锁定铁律\*\*：[\s\S]*?\n6\. \*\*不写显式 negative\*\*——/,
    `${backgroundLockRule}\n6. **不写显式 negative**——`,
  )

  // 微动作池描述
  prompt = prompt.replace('78 张，9 段从中各选不同', `78 张，${n} 段从中各选不同`)
  prompt = prompt.replace('9 段输出中每段必须', `${n} 段输出中每段必须`)

  // 互斥铁律
  prompt = prompt.replace('9 段之间允许', `${n} 段之间允许`)
  prompt = prompt.replace('与其余 8 段', `与其余 ${n - 1} 段`)
  prompt = prompt.replaceAll('9 张全是', `${n} 张全是`)

  // 质量要求
  prompt = prompt.replace('你的 9 段 imagePrompt', `你的 ${n} 段 imagePrompt`)

  // 强制互斥校验占位符替换
  prompt = prompt.replaceAll('__SHOT_COUNT__', String(n))
  prompt = prompt.replace('9 段 imagePrompt 之间的姿势', `${n} 段 imagePrompt 之间的姿势`)
  prompt = prompt.replace('9 个 shotId', `${n} 个 shotId`)

  // few-shot 段尾
  prompt = prompt.replace(
    '实际形态。你的 9 段 imagePrompt 必须达到或',
    `实际形态。你的 ${n} 段 imagePrompt 必须达到或`,
  )

  // 输出 JSON 示例
  const exampleRoles = buildExampleRoles(n)
  const jsonEntries = Array.from({ length: n }, (_, i) =>
    `    { "shotId": "shot_${i + 1}", "role": "${exampleRoles[i % exampleRoles.length]}", "imagePrompt": "..." }`,
  ).join(',\n')
  const jsonExample = `{\n  "shots": [\n${jsonEntries}\n  ]\n}`
  prompt = prompt.replace(
    /\{\n\s+"shots": \[[\s\S]*?\]\n\}/,
    jsonExample,
  )

  // 输出说明
  prompt = prompt.replace(
    '后端仅把 imagePrompt 字段的纯自然语言传给出图模型。9 段 imagePrompt',
    `后端仅把 imagePrompt 字段的纯自然语言传给出图模型。${n} 段 imagePrompt`,
  )
  if (outCount > 0) {
    prompt = prompt.replace(
      '动态组合 7 张参考 / 棚拍基调卡和 2 张',
      `动态组合 ${refCount} 张参考 / 棚拍基调卡和 ${outCount} 张`,
    )
    prompt = applyLargeCountRequiredGuidance(prompt)
  } else {
    prompt = prompt.replace(
      '动态组合 7 张参考 / 棚拍基调卡和 2 张\n无云蓝天草地外景卡',
      `动态组合 ${refCount} 张参考 / 棚拍基调卡`,
    )
    prompt = prompt.replace('；外景卡不固定到任何 shotId。', '。')
    prompt = removeOutdoorOnlyGuidance(prompt)
  }

  return prompt
}

function buildSceneRuleText(
  resultCount: number,
  referenceCount: number,
  outdoorCount: number,
): string {
  if (outdoorCount === 0) {
    return `本轮 ${resultCount} 张全部沿用参考图拍摄环境基调并弱化成低存在感陪衬；如果参考图本身是白底 / 浅灰白 / 浅米白棚拍，就强化这种干净棚拍感；如果参考图本身有室内场景，就只保留空间基调，不主动加入沙发、绘本、玩具、窗帘、绿植等生活道具。本轮不生成无云蓝天草地外景补充图。`
  }
  return `本轮 ${resultCount} 张中必须有 ${referenceCount} 张沿用参考图拍摄环境基调并弱化成低存在感陪衬；如果参考图本身是白底 / 浅灰白 / 浅米白棚拍，就强化这种干净棚拍感；如果参考图本身有室内场景，就只保留空间基调，不主动加入沙发、绘本、玩具、窗帘、绿植等生活道具。本轮还必须有 ${outdoorCount} 张无云蓝天草地外景补充图，外景位置由抽卡决定，不固定到任何 shot 编号；外景只能作为商品轮播图 / 详情页的干净种草素材，不能替代核心棚拍商品图，且必须一张全身或近全身站姿、一张草地坐姿。`
}

