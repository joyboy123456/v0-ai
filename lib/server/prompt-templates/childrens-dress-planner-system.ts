/**
 * 场景分布：按 resultCount 计算参考/棚拍和蓝天白云草地外景数量。
 */
function getSceneDistribution(count: number): { reference: number; outdoor: number } {
  const outdoor = count >= 9 ? 2 : 0
  return { reference: count - outdoor, outdoor }
}

/**
 * 按 resultCount 生成 Planner 系统提示词。
 *
 * resultCount = 9 时返回原始常量（零开销向后兼容）。
 * resultCount = 10 时做针对性文本替换（9→10, 7→8）。
 */
export function buildChildrensDressPlannerSystemPrompt(
  resultCount: number,
): string {
  if (resultCount === 9) return CHILDRENS_DRESS_PLANNER_SYSTEM_PROMPT

  const n = resultCount
  const { reference: refCount, outdoor: outCount } = getSceneDistribution(n)

  let prompt = CHILDRENS_DRESS_PLANNER_SYSTEM_PROMPT

  // 开头：输出 N 段
  prompt = prompt.replace(
    '你为这套连衣裙输出\n9 段商品展示优先的童装上架大片提示词',
    `你为这套连衣裙输出\n${n} 段商品展示优先的童装上架大片提示词`,
  )

  // 背景常量：场景分布
  if (outCount === 0) {
    prompt = prompt.replace(
      /本轮 9 张中必须有 7 张沿用参考图拍摄环境基调[\s\S]*?不能替代核心棚拍商品图。/,
      `本轮 ${n} 张全部沿用参考图拍摄环境基调并弱化成低存在感陪衬；如果参考图本身是白底 / 浅灰白 / 浅米白棚拍，就强化这种干净棚拍感；如果参考图本身有室内场景，就只保留空间基调，不主动加入沙发、绘本、玩具、窗帘、绿植等生活道具。`,
    )
  } else {
    prompt = prompt.replace('本轮 9 张中必须有 7 张', `本轮 ${n} 张中必须有 ${refCount} 张`)
    prompt = prompt.replace('本轮还必须有 2 张蓝天白云草地外景补充图', `本轮还必须有 ${outCount} 张蓝天白云草地外景补充图`)
  }

  // 标题：N 张图随机裂变规则
  prompt = prompt.replace('9 张图随机裂变规则', `${n} 张图随机裂变规则`)

  // 场景硬约束
  if (outCount === 0) {
    prompt = prompt.replace(
      /\*\*唯一的硬约束（场景比例）\*\*：\n-\n \*\*7 张\*\*[\s\S]*?（不能两张都堆在最后）\n/,
      `**唯一的硬约束（场景比例）**：\n- **${n} 张**全部沿用参考图原本的拍摄环境/棚拍基调（默认审美：白色 / 浅灰白 / 浅米白棚拍），背景写成干净、低存在感、可上架的商品图陪衬\n`,
    )
  } else {
    prompt = prompt.replace('**7 张**沿用参考图', `**${refCount} 张**沿用参考图`)
    prompt = prompt.replace('**2 张**蓝天白云草地外景', `**${outCount} 张**蓝天白云草地外景`)
  }

  // 微动作池描述
  prompt = prompt.replace('24 张，9 段从中各选不同', `24 张，${n} 段从中各选不同`)

  // 互斥铁律
  prompt = prompt.replace('9 段之间允许', `${n} 段之间允许`)
  prompt = prompt.replace('与其余 8 段', `与其余 ${n - 1} 段`)

  // 质量要求
  prompt = prompt.replace('你的 9 段 imagePrompt', `你的 ${n} 段 imagePrompt`)
  prompt = prompt.replace('9 段 imagePrompt 之间的姿势', `${n} 段 imagePrompt 之间的姿势`)

  // few-shot 段尾
  prompt = prompt.replace(
    '实际形态。你的 9 段 imagePrompt 必须达到或',
    `实际形态。你的 ${n} 段 imagePrompt 必须达到或`,
  )

  // 输出 JSON 示例
  const exampleRoles = [
    '轻提裙摆展示', '上半身版型细节', '主图候选全身站姿', '裙摆详情展示',
    '侧身廓形展示', '表情手势商品特写', '坐姿铺裙展示', '双手轻拉裙摆',
    '轻拢发丝展示', '外景蓝天草地展示',
  ]
  const jsonEntries = Array.from({ length: n }, (_, i) =>
    `    { "shotId": "shot_${i + 1}", "role": "${exampleRoles[i % exampleRoles.length]}", "imagePrompt": "..." }`,
  ).join(',\n')
  const jsonExample = `{\n  "shots": [\n${jsonEntries}\n  ]\n}`
  prompt = prompt.replace(
    /\{\n\s+"shots": \[[\s\S]*?\]\n\}/,
    jsonExample,
  )

  // 输出说明
  prompt = prompt.replace(
    '后端仅把 imagePrompt 字段的纯自然语言传给出图模型。9 段 imagePrompt',
    `后端仅把 imagePrompt 字段的纯自然语言传给出图模型。${n} 段 imagePrompt`,
  )
  if (outCount > 0) {
    prompt = prompt.replace(
      '动态组合 7 张参考 / 棚拍基调卡和 2 张',
      `动态组合 ${refCount} 张参考 / 棚拍基调卡和 ${outCount} 张`,
    )
  } else {
    prompt = prompt.replace(
      '动态组合 7 张参考 / 棚拍基调卡和 2 张\n蓝天白云草地外景卡',
      `动态组合 ${refCount} 张参考 / 棚拍基调卡`,
    )
  }

  // 电商逻辑兜底
  prompt = prompt.replace(
    '局部 / 半身详情素材不超过 3 张',
    `局部 / 半身详情素材不超过 ${Math.max(1, Math.floor(n * 0.3))} 张`,
  )

  return prompt
}

