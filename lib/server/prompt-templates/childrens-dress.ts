/**
 * 按二级品类和出图数量返回对应蓝图。
 *
 * 场景分布规则：
 * - 2 张：正面全身站姿 + 侧面全身站姿，全部 reference（无外景）
 * - 4 张：2 正面全身站姿 + 1 侧面全身站姿 + 1 背面全身站姿，全部 reference（无外景）
 * - 9 张：1 金属椅坐姿 + 1 背面展示变化位（透明椅坐姿 / 地面坐姿 / 背面站姿）+ 5 全身 reference + 1 站姿 outdoor + 1 草地坐姿 outdoor
 * - 10 张：1 金属椅坐姿 + 1 背面展示变化位（透明椅坐姿 / 地面坐姿 / 背面站姿）+ 6 全身 reference + 1 站姿 outdoor + 1 草地坐姿 outdoor
 */
export function getChildrensCategoryShotBlueprintForCount(
  childrensCategory: PhotoFissionChildrensCategory,
  resultCount: number,
): ReadonlyArray<ChildrensCategoryShotBlueprint> | undefined {
  if (childrensCategory !== 'dress') return undefined

  if (resultCount === 2) {
    return CHILDRENS_DRESS_TWO_SHOT_BLUEPRINT
  }
  if (resultCount === 4) {
    return CHILDRENS_DRESS_FOUR_SHOT_BLUEPRINT
  }

  const outdoorCount = resultCount >= 9 ? 2 : 0
  const referenceCount = resultCount - outdoorCount
  const requiredReferenceShots = resultCount >= 9
    ? [
      CHILDRENS_DRESS_SEATED_CHAIR_SHOT,
      ...takeRandom(CHILDRENS_DRESS_BACK_DISPLAY_VARIATION_POOL, 1),
    ]
    : []
  const referencePoolCount = Math.max(referenceCount - requiredReferenceShots.length, 0)

  const referenceShots = takeRandom(
    CHILDRENS_DRESS_FULL_BODY_REFERENCE_SHOT_POOL,
    Math.min(referencePoolCount, CHILDRENS_DRESS_FULL_BODY_REFERENCE_SHOT_POOL.length),
  )
  const outdoorShots = outdoorCount > 0
    ? [
      ...takeRandom(CHILDRENS_DRESS_FULL_BODY_OUTDOOR_SHOT_POOL, 1),
      CHILDRENS_DRESS_OUTDOOR_SEATED_GRASS_SHOT,
    ].slice(0, outdoorCount)
    : []

  return shuffle([...requiredReferenceShots, ...referenceShots, ...outdoorShots])
}

