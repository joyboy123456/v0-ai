function buildChildrensDressShotBlueprint(): ReadonlyArray<ChildrensCategoryShotBlueprint> {
  const referenceShots = [
    CHILDRENS_DRESS_SEATED_CHAIR_SHOT,
    ...takeRandom(CHILDRENS_DRESS_BACK_DISPLAY_VARIATION_POOL, 1),
    ...takeRandom(CHILDRENS_DRESS_FULL_BODY_REFERENCE_SHOT_POOL, 5),
  ]
  const outdoorShots = [
    ...takeRandom(CHILDRENS_DRESS_FULL_BODY_OUTDOOR_SHOT_POOL, 1),
    CHILDRENS_DRESS_OUTDOOR_SEATED_GRASS_SHOT,
  ]
  return shuffle([...referenceShots, ...outdoorShots])
}

function takeRandom<T>(items: ReadonlyArray<T>, count: number): T[] {
  return shuffle([...items]).slice(0, count)
}

function shuffle<T>(items: T[]): T[] {
  for (let index = items.length - 1; index > 0; index -= 1) {
    const swapIndex = Math.floor(Math.random() * (index + 1))
    const current = items[index]
    items[index] = items[swapIndex]
    items[swapIndex] = current
  }
  return items
}

